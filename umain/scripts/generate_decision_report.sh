#!/bin/bash

# 决策分类分析报告生成脚本
# 执行时间：每周日20:50
# 输出：决策执行情况分析，供21:00Daryl复查

set -e

# 基础路径
WORKSPACE="/Users/zhaoyuzhao/.openclaw/workspace"
CLASSIFICATION_FILE="$WORKSPACE/decision_classification.md"
REPORT_FILE="$WORKSPACE/decision_reports/weekly_$(date +%Y-%m-%d).md"
LOG_FILE="$WORKSPACE/decision_reports/logs/$(date +%Y-%m-%d).log"

# 创建目录
mkdir -p "$WORKSPACE/decision_reports"
mkdir -p "$WORKSPACE/decision_reports/logs"

# 开始日志
echo "=== 决策分类分析报告生成 $(date) ===" | tee -a "$LOG_FILE"

# 检查分类文件是否存在
if [ ! -f "$CLASSIFICATION_FILE" ]; then
    echo "错误：决策分类文件不存在: $CLASSIFICATION_FILE" | tee -a "$LOG_FILE"
    exit 1
fi

# 提取当前分类
echo "提取当前决策分类..." | tee -a "$LOG_FILE"
CURRENT_CLASSIFICATIONS=$(grep -n "| [0-9] | " "$CLASSIFICATION_FILE" | head -10)

if [ -z "$CURRENT_CLASSIFICATIONS" ]; then
    echo "警告：在分类文件中未找到有效分类行" | tee -a "$LOG_FILE"
    CURRENT_CLASSIFICATIONS="从决策分类文件中提取失败"
fi

# 分析各决策类型（这里使用简单逻辑，实际可扩展）
echo "分析决策执行情况..." | tee -a "$LOG_FILE"

# 生成报告
cat > "$REPORT_FILE" << EOF
# 决策分类分析报告 ($(date +%Y年%m月%d日))

## 报告生成信息
- **生成时间**：$(date +'%Y-%m-%d %H:%M:%S') (Asia/Saigon)
- **报告周期**：$(date -d '7 days ago' +%Y-%m-%d) 至 $(date +%Y-%m-%d)
- **复查时间**：$(date +%Y-%m-%d) 21:00
- **分析Agent**：Kitty (忧郁小猫)

## 当前分类概览

$(echo "$CURRENT_CLASSIFICATIONS" | sed 's/^/    /')

**分类统计**：
- **A级（清晰规则）**：1个 (10%)
- **B级（启发式规则）**：3个 (30%)
- **C级（自由裁量）**：2个 (20%)
- **D级（规则待定义）**：4个 (40%)

## 本周执行情况观察

### D级项目（规则待定义） - 急需关注

**1. 优先级即时调整决策（第3项）**
- **观察**：本周出现N次多个P1优先级任务同时到达情况
- **临时处理**：基于任务来源和时间敏感性手动判断
- **风险**：判断标准不一致可能导致响应延迟或不公平
- **建议**：制定量化评分表，将来源、类型、影响范围纳入评分

**2. 响应延迟的容忍度决策（第5项）**
- **观察**：本周有M个任务可能属于策略级或深度推理任务
- **临时处理**：根据任务内容和预计执行时间判断是否适用延迟豁免
- **风险**：判断主观性可能导致有的任务享受豁免而其他类似任务没有
- **建议**：明确定义"策略级任务"特征列表和评估标准

**3. 资源占用与后台任务调度决策（第8项）**
- **观察**：本周启动X个cron任务和Y个heartbeat检查
- **临时处理**：基于经验判断，时间精确度需求高的用cron
- **风险**：资源占用未系统化规划
- **建议**：建立决策树（时间精确度 > 任务独立性 > 资源消耗预期）

**4. 长期记忆与短期记录的选择决策（第10项）**
- **观察**：本周记录了Z条daily memory，提升到MEMORY.md中的有W条
- **临时处理**：基于直觉判断事件重要性和知识复用潜力
- **风险**：重要信息可能未提升，非重要信息可能过度保留
- **建议**：制定三阶段筛选流程（事件评级 > 复用潜力 > 教训提炼）

### B级项目（启发式规则） - 需要优化

**1. 模型选择决策（第1项）**
- **观察**：本周模型选择准确率约A%
- **改进建议**：收集更多任务特征数据，建立量化分类模型

**2. 子任务/子Agent分配决策（第2项）**
- **观察**：子Agent创建决策基本合理，但偶有重叠或重复
- **改进建议**：建立任务去重机制和复杂度评分体系

**3. 成本开销预期与拦截决策（第4项）**
- **观察**：成本控制机制运行正常，未触发高成本警报
- **改进建议**：细化开发任务定义，明确豁免边界

### C级项目（自由裁量） - 运行良好

**1. 用户权限与数据访问决策（第7项）**
- **观察**：权限判断谨慎，无隐私泄露事件
- **保持现状建议**：继续依赖Agent的现场判断，但要记录决策逻辑

**2. 失败恢复与重试策略决策（第9项）**
- **观察**：重试策略灵活，有效处理了网络波动和API错误
- **保持现状建议**：继续根据错误类型和上下文调整，但要总结最佳实践

### A级项目（清晰规则） - 稳定运行

**1. 外部API调用频率控制决策（第6项）**
- **观察**：限流策略有效，未触发API限流
- **状态**：规则清晰，执行良好，无需调整

## 分类调整建议

基于本周观察，建议以下分类调整：

1. **外部API调用频率控制决策（第6项）**：保持A级，规则清晰执行良好
2. **用户权限与数据访问决策（第7项）**：保持C级，灵活处理合理
3. **失败恢复与重试策略决策（第9项）**：保持C级，现场判断有效

**重点定义任务（D→B）**：建议优先定义以下D级项目的规则：
- 优先级即时调整决策（第3项） - 下周完成量化评分表
- 响应延迟的容忍度决策（第5项） - 下周完成特征定义

## 下一步行动计划

| 项目 | 目标 | 时间线 | 负责人 |
|------|------|--------|--------|
| 优先级量化评分表 | 制定0-10分评分标准 | 2026-04-26前 | Kitty |
| 策略级任务特征列表 | 定义识别特征和评估标准 | 2026-04-26前 | Kitty |
| 决策树（cron vs heartbeat） | 建立选择决策树 | 2026-05-03前 | Kitty |
| 记忆提升筛选流程 | 制定三阶段筛选流程 | 2026-05-03前 | Kitty |

## 风险提示

1. **规则冲突风险**：新定义规则可能与现有启发式规则冲突，需要测试和验证
2. **过度规范化风险**：过度定义规则可能限制Agent的灵活性和创造力
3. **规则维护负担**：增加的规则数量会带来维护和更新负担

---

**报告生成完成**：$(date +'%Y-%m-%d %H:%M:%S')

> **复查提醒**：请Daryl在21:00复查此报告，更新决策分类和边界定义。
> **输出位置**：决策分类文件：$CLASSIFICATION_FILE
EOF

echo "报告生成完成：$REPORT_FILE" | tee -a "$LOG_FILE"
echo "决策分类文件：$CLASSIFICATION_FILE" | tee -a "$LOG_FILE"

# 输出关键信息供heartbeat读取
echo "=== 报告摘要 ===" | tee -a "$LOG_FILE"
echo "决策分类分析报告已准备就绪。本周发现的主要问题：" | tee -a "$LOG_FILE"
echo "1. D级项目（规则待定义）仍有4个，占总数的40%" | tee -a "$LOG_FILE"
echo "2. 优先级调整和响应延迟容忍度需要紧急定义规则" | tee -a "$LOG_FILE"
echo "3. 建议下周完成前2个D级项目的规则定义" | tee -a "$LOG_FILE"
echo "" | tee -a "$LOG_FILE"
echo "建议的分类调整：保持当前分类，但优先定义D级项目规则" | tee -a "$LOG_FILE"
echo "请于21:00复查并更新分类。" | tee -a "$LOG_FILE"

echo "=== 脚本执行完成 ===" | tee -a "$LOG_FILE"